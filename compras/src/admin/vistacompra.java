    private void BmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BmostrarActionPerformed
        compra.setId(Integer.parseInt(Tid.getText()));/*se manda a llamar el objeto por medio del set*/
        compra.setNombres(Tname.getText());
        compra.setProducto(Tproducto.getText());
        compra.setValor(Double.parseDouble(Tvalor.getText()));
        compra.setCantidad(Integer.parseInt(Tcantidad.getText()));
        compra.totalCompra();
        
        
        
        JOptionPane.showMessageDialog(null, "cliente id: "+compra.getId()+"\n"+
                "cliente nombre: "+compra.getNombres()+
                "producto: "+compra.getProducto()+"\n"+"valor unitario del producto"+compra.getValor()+"\n"+
                "Cantidad a comprar: "+compra.getCantidad()+"\n"+"Total a pagar: "+compra.totalCompra());
        
        Lmostrar.setText("cliente id: "+compra.getId()+"\n"+"cliente nombre: "+compra.getNombres()+"\n"+
                "producto: "+compra.getProducto()+"\n"+"valor unitario del producto"+compra.getValor()+"\n"+
                "Cantidad a comprar: "+compra.getCantidad()+"\n"+"Total a pagar: "+compra.totalCompra());
    }//GEN-LAST:event_BmostrarActionPerformed

